public class Personas
{
    private String nombre;
    private Asignador asignado;
    /**
     * Constructor for objects of class Personas
     */
    public Personas(String nombre)
    {
        this.nombre = nombre;
        asignado.guardarPersona(nombre);
    }

    /**
     * 
     */
    public String getNombre()
    {     
        return nombre;
    }
}
